      <li>Extract the files from the zip.</li>
      <li>Format your SD Card (explained later!)</li>
    </ol>
  </p>
  <br />
<h6 style="text-align: center;">Drag and Drop to your SD card</h6>
<p>
<ol>
  <li>Make sure to safely eject your SD card</li>
  <li>Plug in your peripherals on your Pi</li>
  <li>Power up your Pi!</li>
  <li>Raspberry Pi will boot and ask if you want to install Operating System. Click install.</li>
  <li>When install is finished hit <b>Tab</b> to finish.</li>
  <li>You will then have to configure to Graphical User Interface which is similar between an OS.</li>
  <li>Once you have configured this you are done. Congrats on first time setup!</li>
</ol>
